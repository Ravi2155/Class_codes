//switch statements

//uses thriple equals sign to compare
const grade ='D';

switch(grade){
    case 'A':
        console.log('you got A');
        break;
    case 'B':
        console.log('you got B');
        break;
    case 'C':
        console.log('you got C');
        break;
    case 'D':
        console.log('you got D');
        break;
    case 'E':
        console.log('you got E');
        break;
    default:
        console.log('not a valid grade');
        break;
}

